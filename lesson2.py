# Задача 3
# Вывести сумму ряда чисел от 1 до 100. Полученный результат вывести на экран.

x = 1  # Для входа в цикл устанавливаем х = 1
summ = 0  # Обнуляем сумму

while x < 101:
    summ = x + summ
    x += 1

print('Сумма ряда чисел от 1 до 100 = ', summ)  # Вывод результата.

