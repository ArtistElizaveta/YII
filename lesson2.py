# Задача 2
# Пользователь в цикле вводит 10 производных цифр. Выведите количество введеных пользователем цифр 5.


count = 0 # Обнуляем счётчик
x = 1 # Для входа в цикл х = 1
while x < 11:
  n = int(input('Введите цифры от 0 до 9: '))
  x += 1
  if n == 5: # Если пользователь ввёл 5
    count += 1 # Счётчик начал работу

print('Количество введенных цифр 5 = ', count)

